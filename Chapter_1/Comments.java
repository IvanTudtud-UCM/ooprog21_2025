public class Comments {
    public static void main(String[] args) {
        
        // Single-line Comment: This prints a welcome message
        System.out.println("Hello World");
        
        /* 
           Multi-line Comment: 
           The next line prints another message. 
           You can use this style for longer explanations.
        */
        System.out.println("Hello World");
        
        /**
         * Documentation Comment:
         * This line shows how doc comments look in code.
         * Normally used for classes and methods.
         */
        System.out.println("Hello World");
    }
}
